    private void jButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActualizarActionPerformed
        String tipo;
        if(jComboBoxTipo.getSelectedItem().toString().equals("Libre")){
            tipo = "0";
        } else if(jComboBoxTipo.getSelectedItem().toString().equals("Usuario")){
            tipo = "1";
        } else {
            tipo = "2";
        }
        
        String genero;
        if(jComboBoxGenero.getSelectedItem().toString().equals("Hombre")){
            genero = "H";
        } else {
            genero = "M";
        }
        
        String cursoEscolar = String.valueOf(jYearChooser1.getYear()) + "/" + String.valueOf(jYearChooser2.getYear());
        
        try {
            Actualizar.actualizarAlumno(a.getIdAlumno(), Integer.parseInt(jTextFieldNIA.getText()),
                jTextFieldNombre.getText(), jTextFieldPApellido.getText(),
                jTextFieldSApellido.getText(), tipo, jTextFieldMotivo.getText(), 
                jTextAreaObservaciones.getText(), genero);
        
            Actualizar.actualizarEstudio(a.getIdAlumno(), cursoEscolar, 
                jComboBoxEnsenyanza.getSelectedItem().toString(), 
                Integer.parseInt(jComboBoxNumCurso.getSelectedItem().toString()), 
                jComboBoxNombreGrupo.getSelectedItem().toString());
            
            JOptionPane.showMessageDialog(null, "Se han actualizado datos con éxito");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "No se ha podido actualizar datos del alumno");
        }
        
    }//GEN-LAST:event_jButtonActualizarActionPerformed


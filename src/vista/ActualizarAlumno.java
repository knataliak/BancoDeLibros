    public void actualiza(Alumno a) {
        jTextFieldNIA.setText(String.valueOf(a.getNia()));
        jTextFieldNombre.setText(a.getNombre());
        jTextFieldPApellido.setText(a.getApellido1());
        jTextFieldSApellido.setText(a.getApellido2());
        
        if(a.getGenero().equals("H")){
            jComboBoxGenero.setSelectedItem("Hombre");
        } else {
            jComboBoxGenero.setSelectedItem("Mujer");
        }
        
        if(a.getTipo() == 0){
            jComboBoxTipo.setSelectedItem("Libre");
        } else if(a.getTipo() == 1) {
            jComboBoxTipo.setSelectedItem("Usuario");
        } else {
            jComboBoxTipo.setSelectedItem("Becado");
        }

        jTextFieldMotivo.setText(a.getMotivoBeca());
        jTextAreaObservaciones.setText(a.getObservaciones());
    }
    
    public void actualizaE(Estudia e) {
        jComboBoxEnsenyanza.setSelectedItem(e.getEnsenyanza());
        jComboBoxNumCurso.setSelectedItem(String.valueOf(e.getNumCurso()));
        jComboBoxNombreGrupo.setSelectedItem(e.getGrupo());
        StringTokenizer st = new StringTokenizer(e.getCursoEscolar());
        while(st.hasMoreTokens()){
            jYearChooser1.setYear(Integer.parseInt(st.nextToken("/")));
            jYearChooser2.setYear(Integer.parseInt(st.nextToken()));
            
        }
    }


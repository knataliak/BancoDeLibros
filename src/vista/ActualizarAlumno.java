    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        //buscar alumnos que corresponden al criterio de la búsqueda
        if(jComboBoxBuscar.getSelectedItem().toString().equals("Por Nombre")){
            //Vaciar volumenes para resultados de nueva búsqueda
            alumnos.borrar();
            
            Seleccionar.buscarAlumnoNombre(jTextFieldBuscar.getText());
            actualiza(c.primero());
            
        } else if(jComboBoxBuscar.getSelectedItem().toString().equals("Por NIA")){
            //Vaciar volumenes para resultados de nueva búsqueda
            alumnos.borrar();
            
            Seleccionar.buscarAlumnoNIA(Integer.parseInt(jTextFieldBuscar.getText()));
            actualiza(c.primero());
        } else if(jComboBoxBuscar.getSelectedItem().toString().equals("Por Primer Apellido")){
            //Vaciar volumenes para resultados de nueva búsqueda
            alumnos.borrar();
            
            Seleccionar.buscarAlumnoPrimerApellido(jTextFieldBuscar.getText());
            actualiza(c.primero());
        }
        
        //obtener ID del Alumno
        a = c.getAlumno();
        estudia.borrar();
        Seleccionar.buscarAlumnoESTUDIA(a.getIdAlumno());
        e = c2.getEstudia();
        actualizaE(e);
        
       
        
        
    }//GEN-LAST:event_jButtonBuscarActionPerformed


    public static void reporteEjemplares () {
        Conexion c = new Conexion();
        
        try {
           
            Map parametros = new HashMap();
            
            if(isWindows()){
                
                JasperReport informe = (JasperReport) JRLoader.loadObjectFromFile(dir + "\\src\\reportes\\stockEjemplares.jasper");
                JasperPrint mostrarInforme = JasperFillManager.fillReport(informe, parametros, c.getConn());
                
                
                JasperViewer view = new JasperViewer(mostrarInforme, false);
                view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
                view.setVisible(true);
            } else {
                JasperReport informe = (JasperReport) JRLoader.loadObjectFromFile(dir + "/src/reportes/stockEjemplares.jasper");
                JasperPrint mostrarInforme = JasperFillManager.fillReport(informe, parametros, c.getConn());
                
                JasperViewer view = new JasperViewer(mostrarInforme, false);
                view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
                view.setVisible(true);
            }
            
        } catch (JRException ex) {
            java.util.logging.Logger.getLogger(RepartirLibros.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


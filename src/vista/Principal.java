    private void verAyuda() {
	try {
            // Carga el fichero de ayuda
            if(isWindows()){
                File fichero = new File(dir + "\\help\\help_set.hs");
                URL hsURL = fichero.toURI().toURL();
                
                // Crea el HelpSet y el HelpBroker
                HelpSet helpset = new HelpSet(getClass().getClassLoader(), hsURL);
                HelpBroker hb = helpset.createHelpBroker();

                // Pone ayuda a item de menu al pulsarlo y a F1 en ventana
                // principal y secundaria.
                hb.enableHelpOnButton(jButtonAlta, "aplicacion", helpset);
                hb.enableHelpKey(getRootPane(), "ventana_principal", helpset);
                hb.enableHelpKey(Ayuda, "ventana_principal", helpset);
            } else {
                File fichero = new File(dir + "/help/help_set.hs");
                URL hsURL = fichero.toURI().toURL();
                
                // Crea el HelpSet y el HelpBroker
                HelpSet helpset = new HelpSet(getClass().getClassLoader(), hsURL);
                HelpBroker hb = helpset.createHelpBroker();

                // Pone ayuda a item de menu al pulsarlo y a F1 en ventana
                // principal y secundaria.
                hb.enableHelpOnButton(jButtonAlta, "aplicacion", helpset);
                hb.enableHelpKey(getRootPane(), "ventana_principal", helpset);
                hb.enableHelpKey(Ayuda, "ventana_principal", helpset);
            }
	} catch (Exception e) {
            e.printStackTrace();
	}
    }

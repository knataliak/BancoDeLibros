    private void jMenuItemAlumnosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemAlumnosActionPerformed
        JFileChooser frame = new JFileChooser();
        frame.setVisible(true);
        frame.showOpenDialog(frame);
        try {
            if (frame.isFileSelectionEnabled()) {
                Conexion.Conectar();
                File fichero = frame.getSelectedFile();
                String f = fichero.getAbsolutePath();
                borrarTablaAlumno();
                importarFicheroAlumnos(f);
                System.out.println(f);
                JOptionPane.showMessageDialog(null, "El fichero se ha importado con éxito");
            }
        } catch (Exception n) {
            JOptionPane.showMessageDialog(null, "No ha eligido ningún fichero");
        }
    }//GEN-LAST:event_jMenuItemAlumnosActionPerformed

